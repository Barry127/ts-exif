import { ExifOptions, ExifValue, Int } from '../../../types';

export function parseLensType(
  value: Int,
  options: ExifOptions
): ExifValue<Int, string> | Int | null {
  if (!isInt(value)) return null;
  if (options.strictValues && !Object.keys(LENS_TYPES_MAP).includes(`${value}`))
    return null;
  if (!options.parseValues) return value;

  const parsedValue = LENS_TYPES_MAP[value] ?? 'Unknown';
  return { original: value, value: parsedValue };
}

